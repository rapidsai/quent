//! Core types for defining Quent application models.
//!
//! This crate provides the foundational types used to define FSMs, resources,
//! entities, and their relationships. Proc macro annotations (provided by
//! `quent-model-macros`) generate instrumentation APIs and metadata from
//! definitions built on these types.

