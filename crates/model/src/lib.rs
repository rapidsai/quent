/// Metadata trait for FSM states, generated by the `state!` macro.
///
/// Provides the state's structural metadata (field names, types, which are
/// usages) so the `fsm!` macro can collect it without needing to inspect the
/// state struct directly.
pub trait StateMetadata {
    /// Returns the state definition for model metadata collection.
    fn state_def() -> model::StateDef;

    /// The canonical name of this state (snake_case derived from struct name).
    fn state_name() -> &'static str;
}

/// Marker trait for types declared as entities via the `entity!` macro.
pub trait Entity {}

/// Associates a model marker type with its event type.
///
/// Generated by `fsm!` and `entity!`.
/// Used by `model!` to auto-generate the top-level event enum.
pub trait HasEventType {
    type Event: Send + 'static;
}

/// Associates a model marker type with its analyzer data struct.
///
/// Generated by `entity!`. The data struct has `Option<T>` fields per event
/// type, populated by `push()`.
pub trait EntityData: HasEventType {
    type Data: Default;

    /// Store an event in the data struct.
    fn push(data: &mut Self::Data, event: Self::Event);
}

/// Metadata trait for entity event structs, generated by `#[derive(Attributes)]`.
///
/// Provides the event's structural metadata (field names, types) so the
/// `entity!` macro can populate `EntityEventDef.attributes` without needing
/// to inspect the event struct directly.
pub trait EventMetadata {
    /// Returns the event definition for model metadata collection.
    fn event_def() -> model::EntityEventDef;
}

/// Marker trait for resource groups declared via `entity!` or `fsm!`.
pub trait ResourceGroup {
    /// Whether this is the root resource group (no parent).
    const IS_ROOT: bool = false;
}

